package com.day_1;

public class Student6 {
	int rollno;
	String name;
	int marks;
	public Student6(int rollno, String name, int marks) {

		this.rollno = rollno;
		this.name = name;
		this.marks = marks;
	}
	@Override
	public String toString() {
		return "Student6 [rollno=" + rollno + ", name=" + name + ", marks=" + marks + "]";
	}
	
	
	
	

}
