package com.day_1;

import java.util.ArrayList;

public class Student8 {
	
	int sid;
	String name;
	ArrayList<String> hobby;
	public Student8(int sid, String name, ArrayList<String> hobby) {
	
		this.sid = sid;
		this.name = name;
		this.hobby = hobby;
	}
	@Override
	public String toString() {
		return "Student8 [sid=" + sid + ", name=" + name + ", hobby=" + hobby + "]";
	}
	
	
	
	

}
